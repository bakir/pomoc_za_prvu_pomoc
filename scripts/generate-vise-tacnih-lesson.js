#!/usr/bin/env node
/**
 * Generates src/data/viseTacnihIznimke.js from katalog1 B-category mismatches.
 *
 * Usage: node scripts/generate-vise-tacnih-lesson.js
 */

import { writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { checkSource, root, SOURCES } from './lib/multiAnswerHints.js';

const katalog1 = SOURCES.find((s) => s.label === 'katalog1');
const { missingHint, hintButSingle } = checkSource(katalog1, { categoryFilter: ['B'] });

const quizIds = [...hintButSingle, ...missingHint]
  .map((item) => item.id)
  .sort((a, b) => Number(a) - Number(b));

const sections = [];

if (hintButSingle.length > 0) {
  sections.push({
    id: 'hint-but-single',
    title: '1. Ima oznaku „(više tačnih odgovora)”, ali je tačan samo jedan odgovor',
    description:
      'Ne birajte više opcija samo zato što vidite ovu oznaku — na ispitu je ipak jedan tačan odgovor.',
    items: hintButSingle.map((item) => ({
      id: item.id,
      badge: 'Samo 1 tačan',
      question: item.question,
      answerCount: item.count,
      type: 'hint-but-single',
    })),
  });
}

if (missingHint.length > 0) {
  sections.push({
    id: 'multi-no-hint',
    title: `${sections.length ? '2' : '1'}. Više tačnih odgovora, ali nema oznake`,
    description:
      'Morate odabrati više opcija iako pitanje nema „(više tačnih odgovora)” u tekstu.',
    items: missingHint.map((item) => ({
      id: item.id,
      badge: `${item.count} tačna`,
      question: item.question,
      answerCount: item.count,
      type: 'multi-no-hint',
    })),
  });
}

const outPath = join(root, 'src/data/viseTacnihIznimke.js');
const content = `// Auto-generated by scripts/generate-vise-tacnih-lesson.js — do not edit by hand.
// Re-run: node scripts/generate-vise-tacnih-lesson.js

export const VISE_TACNIH_QUIZ_IDS = ${JSON.stringify(quizIds, null, 2)};

export const VISE_TACNIH_SECTIONS = ${JSON.stringify(sections, null, 2)};
`;

writeFileSync(outPath, content, 'utf8');

console.log(`Wrote ${outPath}`);
console.log(`  Hint but single answer: ${hintButSingle.length}`);
console.log(`  Multi-answer without hint: ${missingHint.length}`);
console.log(`  Quiz questions: ${quizIds.length}`);
